thin = Side(style="thin", color="CCCCCC")
border = Border(left=thin, right=thin, top=thin, bottom=thin)

def header_fill(color): return PatternFill("solid", fgColor=color)
def cell_font(bold=False, color="000000", size=10):
    return Font(name="맑은 고딕", bold=bold, color=color, size=size)
def center(): return Alignment(horizontal="center", vertical="center", wrap_text=True)
def left():   return Alignment(horizontal="left",   vertical="center", wrap_text=True)

def write_header(ws, row, cols, bg, fg="FFFFFF"):
    for c, (col, text) in enumerate(cols, 1):
        cell = ws.cell(row=row, column=c, value=text)
        cell.font = cell_font(bold=True, color=fg, size=10)
        cell.fill = header_fill(bg)
        cell.alignment = center()
        cell.border = border
        ws.column_dimensions[get_column_letter(c)].width = col

def write_row(ws, row, values, bg="FFFFFF"):
    fill = header_fill(bg)
    for c, v in enumerate(values, 1):
        cell = ws.cell(row=row, column=c, value=v)
        cell.font = cell_font(size=10)
        cell.fill = fill
        cell.alignment = left()
        cell.border = border

def section_row(ws, row, text, ncols, bg):
    ws.merge_cells(start_row=row, start_column=1, end_row=row, end_column=ncols)
    cell = ws.cell(row=row, column=1, value=text)
    cell.font = cell_font(bold=True, color="FFFFFF", size=10)
    cell.fill = header_fill(bg)
    cell.alignment = left()
    cell.border = border


